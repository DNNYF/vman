# Actor types tell us where the action originated. MCP and CLI entries
# should be present alongside user-issued actions so a forensic review
# can answer "who did this, from where, on whose behalf?".
ACTOR_TYPES: frozenset[str] = frozenset({"user", "system", "mcp", "cli"})


class AuditEvent(Base):
    """Immutable audit record. Updated_at is omitted -- entries are append-only."""

    __tablename__ = "audit_events"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    actor_user_id: Mapped[str | None] = mapped_column(String(64), nullable=True)
    actor_type: Mapped[str] = mapped_column(String(16), nullable=False)
    action: Mapped[str] = mapped_column(String(128), nullable=False)
    resource_type: Mapped[str] = mapped_column(String(64), nullable=False)
    resource_id: Mapped[str] = mapped_column(String(64), nullable=False, default="")
    ip_address: Mapped[str | None] = mapped_column(String(64), nullable=True)
    user_agent: Mapped[str | None] = mapped_column(String(512), nullable=True)
    metadata_json: Mapped[dict] = mapped_column(JSON, nullable=False, default=dict)
    previous_hash: Mapped[str] = mapped_column(String(64), nullable=False, default="")
    event_hash: Mapped[str] = mapped_column(String(64), nullable=False, default="")
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow, index=True
    )

    __table_args__ = (
        CheckConstraint(
            "actor_type IN ('user', 'system', 'mcp', 'cli')",
            name="ck_audit_events_actor_type",
        ),
        Index("ix_audit_events_action", "action"),
        Index("ix_audit_events_resource", "resource_type", "resource_id"),
        Index("ix_audit_events_event_hash", "event_hash"),
    )


# --------------------------------------------------------------------------- #
# Users and authentication
# --------------------------------------------------------------------------- #


# User roles. owner is the bootstrap admin; admin can manage users + hosts;
# operator can run jobs; viewer is read-only.
USER_ROLES: frozenset[str] = frozenset({"owner", "admin", "operator", "viewer"})


class User(Base):
    """Local user account. Passwords are stored as Argon2id hashes."""

    __tablename__ = "users"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    username: Mapped[str] = mapped_column(String(64), nullable=False, unique=True)
    # Email is optional but commonly used as a contact / recovery channel.
    email: Mapped[str | None] = mapped_column(String(254), nullable=True)
    # Argon2id hash (never the plaintext). Nullable for passkey-only accounts
    # in future revisions -- for MVP we require a password.
    password_hash: Mapped[str | None] = mapped_column(String(512), nullable=True)
    # TOTP secret is stored encrypted via the vault in M1/T7; for now the
    # column is reserved and the value defaults to NULL.
    totp_secret_encrypted: Mapped[str | None] = mapped_column(String(1024), nullable=True)
    totp_enabled: Mapped[bool] = mapped_column(
        # SQLite stores bool as INTEGER; SQLAlchemy handles the conversion.
        nullable=False,
        default=False,
    )
    role: Mapped[str] = mapped_column(String(16), nullable=False, default="owner")
    # disabled_at non-null => account is locked (audit must capture the reason).
    disabled_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    # Last successful login (UTC). Used by the audit dashboard.
    last_login_at: Mapped[dt.datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow
    )
    updated_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow, onupdate=_utcnow
    )

    sessions: Mapped[list[UserSession]] = relationship(
        back_populates="user", cascade="all, delete-orphan"
    )
    recovery_codes: Mapped[list[RecoveryCode]] = relationship(
        back_populates="user", cascade="all, delete-orphan"
    )

    __table_args__ = (
        CheckConstraint(
            "role IN ('owner', 'admin', 'operator', 'viewer')",
            name="ck_users_role",
        ),
    )


class UserSession(Base):
    """Server-side session record.

    The client only ever sees an opaque random token (HttpOnly cookie).
    We store the SHA-256 hash of the token in ``session_token_hash`` so a
    database compromise does NOT yield usable session cookies.
    """

    __tablename__ = "sessions"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    user_id: Mapped[str] = mapped_column(String(64), ForeignKey("users.id"), nullable=False)
    session_token_hash: Mapped[str] = mapped_column(String(128), nullable=False, unique=True)
    # Hash of the double-submit CSRF cookie. Stored so we can rotate
    # / revoke the CSRF token independently of the session token.
    csrf_token_hash: Mapped[str | None] = mapped_column(String(128), nullable=True)
    ip_address: Mapped[str | None] = mapped_column(String(64), nullable=True)
    user_agent: Mapped[str | None] = mapped_column(String(512), nullable=True)
    expires_at: Mapped[dt.datetime] = mapped_column(DateTime(timezone=True), nullable=False)
    revoked_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow
    )

    user: Mapped[User] = relationship(back_populates="sessions")

    __table_args__ = (
        Index("ix_sessions_user_id", "user_id"),
        Index("ix_sessions_expires_at", "expires_at"),
    )


class RecoveryCode(Base):
    """One-time recovery code used when the user loses TOTP / passkey."""

    __tablename__ = "recovery_codes"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    user_id: Mapped[str] = mapped_column(String(64), ForeignKey("users.id"), nullable=False)
    # SHA-256 hash of the recovery code (plaintext is shown ONCE at creation).
    code_hash: Mapped[str] = mapped_column(String(128), nullable=False)
    used_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow
    )

    user: Mapped[User] = relationship(back_populates="recovery_codes")

    __table_args__ = (Index("ix_recovery_codes_user_id", "user_id"),)


