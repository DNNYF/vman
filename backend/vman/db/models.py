"""SQLAlchemy ORM models for VMAN (Milestone 0 / Task 2).

This module declares the foundational tables that other milestones depend
on. New tables for users/sessions/hosts/jobs/recipes land in the
milestones that introduce them so each migration matches the code that
exercises the model.

Conventions:

- Every primary key is a string UUID generated by the application.
- Every timestamp is timezone-aware UTC (``DateTime(timezone=True)``).
- Encrypted payloads are stored as BLOBs -- the application never reads
  plaintext from the database.
- JSON-shaped columns are typed as JSON (SQLite stores as TEXT, Postgres
  as JSONB).
"""

from __future__ import annotations

import datetime as dt

from sqlalchemy import (
    JSON,
    CheckConstraint,
    DateTime,
    ForeignKey,
    Index,
    Integer,
    LargeBinary,
    String,
    UniqueConstraint,
)
from sqlalchemy.orm import Mapped, mapped_column, relationship

from vman.db.base import Base

# --------------------------------------------------------------------------- #
# Helpers
# --------------------------------------------------------------------------- #


def _utcnow() -> dt.datetime:
    """Timezone-aware UTC now -- never naive datetimes on the wire."""
    return dt.datetime.now(dt.timezone.utc)


# --------------------------------------------------------------------------- #
# Encryption key registry
# --------------------------------------------------------------------------- #


class EncryptionKey(Base):
    """Versioned record of every master key the vault has used.

    The actual key material is NEVER stored here -- it lives in
    ``Settings.master_key`` (env). This table is the index that lets us
    decrypt old payloads after rotation.
    """

    __tablename__ = "encryption_keys"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    version: Mapped[int] = mapped_column(Integer, nullable=False, unique=True)
    # active | rotated | revoked
    status: Mapped[str] = mapped_column(String(16), nullable=False, default="active")
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow
    )
    rotated_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)

    credentials: Mapped[list[Credential]] = relationship(back_populates="encryption_key")

    __table_args__ = (
        CheckConstraint(
            "status IN ('active', 'rotated', 'revoked')",
            name="ck_encryption_keys_status",
        ),
    )


# --------------------------------------------------------------------------- #
# Credential vault
# --------------------------------------------------------------------------- #


# The set of credential kinds we know how to use. Rejecting unknown kinds
# at the DB layer (not just Python) protects against typos creeping in
# via raw SQL or migrations.
CREDENTIAL_KINDS: frozenset[str] = frozenset(
    {
        "ssh_password",
        "ssh_private_key",
        "ssh_private_key_passphrase",
        "sudo_password",
        "api_token",
    }
)


class Credential(Base):
    """An encrypted credential (SSH password, SSH key, API token, ...).

    ``encrypted_payload`` is the ciphertext produced by ``Vault.encrypt``;
    it is always non-null, and the application never decrypts a credential
    outside the worker process.
    """

    __tablename__ = "credentials"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    name: Mapped[str] = mapped_column(String(128), nullable=False, unique=True)
    kind: Mapped[str] = mapped_column(String(32), nullable=False)
    encrypted_payload: Mapped[bytes] = mapped_column(LargeBinary, nullable=False)
    encryption_key_id: Mapped[str] = mapped_column(
        String(64), ForeignKey("encryption_keys.id"), nullable=False
    )
    # Non-secret metadata: e.g. the SSH key fingerprint (sha256 of the
    # public half), the credential owner for display, etc.
    fingerprint: Mapped[str] = mapped_column(String(128), nullable=False, default="")
    metadata_json: Mapped[dict] = mapped_column(JSON, nullable=False, default=dict)
    last_used_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    rotated_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    disabled_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow
    )
    updated_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow, onupdate=_utcnow
    )

    encryption_key: Mapped[EncryptionKey] = relationship(back_populates="credentials")

    __table_args__ = (
        CheckConstraint(
            "kind IN ('ssh_password', 'ssh_private_key', "
            "'ssh_private_key_passphrase', 'sudo_password', 'api_token')",
            name="ck_credentials_kind",
        ),
        Index("ix_credentials_kind", "kind"),
    )


