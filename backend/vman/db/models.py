HOST_AUTH_METHODS: frozenset[str] = frozenset({"password", "key", "key_with_passphrase"})
HOST_SUDO_MODES: frozenset[str] = frozenset({"root", "passwordless_sudo", "sudo_password"})
HOST_ENVIRONMENTS: frozenset[str] = frozenset({"experiment", "staging", "production"})


class Host(Base):
    """A target VPS. No agent runs on the host -- only SSH + shell.

    Sensitive fields (fingerprint, notes) live alongside encrypted
    credential rows in the vault; we never put decrypted values in
    the host row itself.
    """

    __tablename__ = "hosts"

    id: Mapped[str] = mapped_column(String(64), primary_key=True)
    name: Mapped[str] = mapped_column(String(64), nullable=False, unique=True)
    hostname_or_ip: Mapped[str] = mapped_column(String(255), nullable=False)
    ssh_port: Mapped[int] = mapped_column(Integer, nullable=False, default=22)
    username: Mapped[str] = mapped_column(String(64), nullable=False)
    auth_method: Mapped[str] = mapped_column(String(32), nullable=False)
    # Optional FK to a vault credential. Not enforced as a hard FK in
    # SQLite to allow hosts to exist before their creds are added.
    credential_id: Mapped[str | None] = mapped_column(String(64), nullable=True)
    sudo_mode: Mapped[str] = mapped_column(String(32), nullable=False, default="root")
    host_key_fingerprint: Mapped[str | None] = mapped_column(String(128), nullable=True)
    host_key_algorithm: Mapped[str | None] = mapped_column(String(16), nullable=True)
    os_family: Mapped[str | None] = mapped_column(String(32), nullable=True)
    os_name: Mapped[str | None] = mapped_column(String(64), nullable=True)
    os_version: Mapped[str | None] = mapped_column(String(64), nullable=True)
    package_manager: Mapped[str | None] = mapped_column(String(32), nullable=True)
    arch: Mapped[str | None] = mapped_column(String(16), nullable=True)
    cpu_cores: Mapped[int | None] = mapped_column(Integer, nullable=True)
    ram_mb: Mapped[int | None] = mapped_column(Integer, nullable=True)
    disk_total_mb: Mapped[int | None] = mapped_column(Integer, nullable=True)
    provider: Mapped[str | None] = mapped_column(String(64), nullable=True)
    region: Mapped[str | None] = mapped_column(String(64), nullable=True)
    environment: Mapped[str] = mapped_column(String(16), nullable=False, default="experiment")
    risk_level: Mapped[str | None] = mapped_column(String(16), nullable=True)
    tags: Mapped[list] = mapped_column(JSON, nullable=False, default=list)
    notes: Mapped[str] = mapped_column(String(2048), nullable=False, default="")
    last_seen_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    disabled_at: Mapped[dt.datetime | None] = mapped_column(DateTime(timezone=True), nullable=True)
    created_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow
    )
    updated_at: Mapped[dt.datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, default=_utcnow, onupdate=_utcnow
    )

    __table_args__ = (
        CheckConstraint(
            "auth_method IN ('password', 'key', 'key_with_passphrase')",
            name="ck_hosts_auth_method",
        ),
        CheckConstraint(
            "sudo_mode IN ('root', 'passwordless_sudo', 'sudo_password')",
            name="ck_hosts_sudo_mode",
        ),
        CheckConstraint(
            "environment IN ('experiment', 'staging', 'production')",
            name="ck_hosts_environment",
        ),
        Index("ix_hosts_tags", "tags"),
        Index("ix_hosts_environment", "environment"),
    )


